#Este laço de repetição while (enquanto em português)
#acrescenta a variável batatas_descascadas 1 até que
#batatas_descascadas seja maior que 10

batatas_descascadas = 0

while(batatas_descascadas < 10):
    batatas_descascadas += 1
    print(batatas_descascadas, "descascadas")

print ("Agora temos batatas o suficiente")


